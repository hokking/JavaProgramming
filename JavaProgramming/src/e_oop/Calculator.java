package e_oop;

public class Calculator {

	long add(long a, long b) {
		return a + b;
	}

	long sub(long a, long b) {
		return a - b;
	}

	long mul(long a, long b) {
		return a * b;
	}

	long div(long a, long b) {
		return a / b;
	}

	long rem(long a, long b) {
		return a % b;
	}
}
